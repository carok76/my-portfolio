document.addEventListener("DOMContentLoaded", function () {
  const picWeather = document.getElementById("pic-weather");

  picWeather.addEventListener("mouseenter", function () {
    picWeather.src = "./src/weather-app-colour.png";
  });

  picWeather.addEventListener("mouseleave", function () {
    picWeather.src = "./src/weather-app-bw.png";
  });
});

